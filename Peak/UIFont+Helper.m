#import "UIFont+Helper.h"

@implementation UIFont (Helper)

+ (UIFont *)dinproBoldFontWithSize:(CGFloat)size {
    return [UIFont fontWithName:@"DINPro-Bold" size:size];
}

+ (UIFont *)dinproRegularFontWithSize:(CGFloat)size {
    return [UIFont fontWithName:@"DINPro-Regular" size:size];
}

@end
